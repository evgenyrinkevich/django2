from django.db import models
from django.conf import settings
from mainapp.models import Product


class Order(models.Model):
    FORMING = 'F'
    SENT_TO_PROCEED = 'S'
    PROCEEDED = 'P'
    PAID = 'D'
    READY = 'R'
    CANCELED = 'C'

    ORDER_STATUS_CHOICES = (
        (FORMING, 'формируется'),
        (SENT_TO_PROCEED, 'отправлен в обработку'),
        (PAID, 'оплачен'),
        (PROCEEDED, 'обрабатывается'),
        (READY, 'готов'),
        (CANCELED, 'отменен'),
    )

    user = models.ForeignKey(settings.AUTH_USER_MODEL, on_delete=models.CASCADE)
    created = models.DateTimeField(verbose_name='создан', auto_now_add=True)
    updated = models.DateTimeField(verbose_name='обновлен', auto_now=True)
    status = models.CharField(verbose_name='статус', max_length=1,
                              choices=ORDER_STATUS_CHOICES,
                              default=FORMING)
    is_active = models.BooleanField(verbose_name='активен', default=True)

    class Meta:
        ordering = ('-created',)
        verbose_name = 'заказ'
        verbose_name_plural = 'заказы'

    def __str__(self):
        return f'Заказ №{self.pk}'

    def get_total_quantity(self):
        items = self.orderitems.select_related('product')
        return sum(map(lambda x: x.quantity, items))

    def get_total_cost(self):
        items = self.orderitems.select_related('product')
        return sum(map(lambda x: x.product_cost, items))

    def get_items(self):
        pass

    def is_forming(self):
        return self.status == Order.FORMING

    def delete(self, using=None, keep_parents=False):
        for item in self.orderitems.select_related('product'):
            item.product.quantity += item.quantity
            item.save()
        self.is_active = False
        self.save()

    def get_summary(self):
        items = self.orderitems.select_related()
        return {
            'get_total_cost': sum(map(lambda x: x.product_cost, items)),
            'get_total_quantity': sum(map(lambda x: x.quantity, items))
        }


class OrderItem(models.Model):
    order = models.ForeignKey(Order, verbose_name='заказ',
                              related_name="orderitems",
                              on_delete=models.CASCADE)
    product = models.ForeignKey(Product, verbose_name='продукты', on_delete=models.CASCADE)
    quantity = models.PositiveIntegerField(verbose_name='количество', default=0)

    @property
    def product_cost(self):
        return self.product.price * self.quantity

